// A pictogram category's most associated muscle group, used to infer a
// group for names that match a keyword but aren't in the exact library.
const CATEGORY_GROUP = {
  Press: 'Chest', Fly: 'Chest', PushUp: 'Chest',
  Row: 'Back', Hinge: 'Back', PullDown: 'Back',
  OverheadPress: 'Shoulders', LateralRaise: 'Shoulders', FrontRaise: 'Shoulders',
  RearDelt: 'Shoulders', Shrug: 'Shoulders', UprightRow: 'Shoulders',
  Curl: 'Biceps',
  TricepExt: 'Triceps', Pushdown: 'Triceps',
  Squat: 'Legs', Lunge: 'Legs', LegPress: 'Legs', LegExt: 'Legs', LegCurl: 'Legs', Calf: 'Legs',
  Plank: 'Core', Crunch: 'Core', LegRaise: 'Core', Twist: 'Core',
  Cardio: 'Cardio',
}

function keywordCategory(name) {
  const nl = name.trim().toLowerCase()
  for (const [kw, cat] of KEYWORD_RULES) {
    if (nl.includes(kw)) return cat
  }
  return null
}

export function groupFor(name) {
  if (!name) return null
  const exact = GROUP_BY_NAME.get(name.trim().toLowerCase())
  if (exact) return exact
  const cat = keywordCategory(name)
  return cat ? CATEGORY_GROUP[cat] || null : null
}

const PICTOGRAM_BY_NAME = new Map(EXERCISES.map((e) => [e.name.toLowerCase(), e.pictogram]))

// Returns the pictogram category key for a name. Tries an exact match
// against the 873-exercise library first, then falls back to the same
// keyword rules used to build that library, so a user's own historical
// wording still resolves. Only genuinely unrecognizable text (e.g. a
// one-off custom name with no matching keyword) returns null.
export function pictogramFor(name) {
  if (!name) return null
  const exact = PICTOGRAM_BY_NAME.get(name.trim().toLowerCase())
  if (exact) return exact
  return keywordCategory(name)
}
